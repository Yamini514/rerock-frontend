export function getLead(id) {
  return leads.find((l) => l.id === id);
}

export function leadsForAgent(agentSlug) {
  return leads.filter((l) => l.agentSlug === agentSlug);
}

/** Builds a {stage, count} funnel array (same shape as lib/data/admin.js's leadFunnel) scoped to one agent.
 * Cumulative over the active pipeline (New -> Won): each stage counts every lead that has reached at
 * least that stage. "Lost" leads are excluded from the funnel — they're surfaced separately as a stat. */
export function leadFunnelForAgent(agentSlug) {
  const scoped = leadsForAgent(agentSlug).filter((l) => l.status !== "Lost");
  const stageOrder = ["New", "Contacted", "Site Visit Scheduled", "Negotiation", "Won"];
  const stageLabel = { New: "New Leads", Contacted: "Contacted", "Site Visit Scheduled": "Site Visit", Negotiation: "Negotiation", Won: "Closed Won" };
  return stageOrder.map((status, i) => ({
    stage: stageLabel[status],
    count: scoped.filter((l) => stageOrder.indexOf(l.status) >= i).length,
  }));
}
